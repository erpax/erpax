#!/usr/bin/env node
/**
 * standards-catalogue — the ONE shared index where the dissolved standards
 * vocabulary meets.
 *
 * Standards are not folders; they are *everywhere*, dissolved across src/ as
 * `@standard` / `@rfc` JSDoc banners (the usage truth). This generator JOINS
 * the curated canonical spine (src/standards/registry.ts — the facts) to a live
 * scan of those banners, and emits ONE catalogue consumed by BOTH frontends:
 *
 *   - payload  → src/standards/seed.ts imports the catalogue → one collection
 *                row per cited standard (the queryable payload index).
 *   - vitepress→ src/standards/SKILL.md gets a generated index section between
 *                <!-- CATALOGUE:START --> / <!-- CATALOGUE:END --> (the served
 *                docs index page).
 *
 * Curated facts ⊕ fs-derived usage. The catalogue is a CACHE; the registry +
 * banners are the truth. Re-run after adding banners or registry rows:
 *   node scripts/standards-catalogue.mjs            (CI: --verify)
 *
 * @standard ISO/IEC-25010:2023 §5.4 reusability (one scan, two consumers)
 * @standard ISO-19011:2018 audit-evidence (citations are the audit trail)
 */
import { execSync } from 'node:child_process'
import { readFileSync, writeFileSync, existsSync } from 'node:fs'

const ROOT = new URL('..', import.meta.url).pathname
const REGISTRY_TS = ROOT + 'src/standards/registry.ts'
const CATALOGUE_TS = ROOT + 'src/standards/catalogue.ts'
const SKILL_MD = ROOT + 'src/standards/SKILL.md'

// --- 1. the curated spine: parse registry.ts (one object literal per line) ---
function registry() {
  const src = readFileSync(REGISTRY_TS, 'utf8')
  const arr = src.match(/STANDARDS_REGISTRY[^=]*=\s*(\[[\s\S]*\])\s*as const/)
  if (!arr) throw new Error('registry.ts: STANDARDS_REGISTRY array literal not found')
  // The captured text is a plain JS array of object literals — evaluate it so
  // the `match` string escapes (\b, \.) carry JS regex semantics, not the
  // double-escaping a raw text-extract would give.
  return new Function('return ' + arr[1])()
}

// --- 2. scan the dissolved vocabulary: every @standard / @rfc banner in src/ ---
function scan() {
  let raw = ''
  try {
    raw = execSync(
      String.raw`rg -n --no-heading -o '@(standard|rfc)\s+[A-Za-z0-9][^*\n]+' src -g '!*.test.ts' -g '!**/catalogue.ts' -g '!**/registry.ts'`,
      { cwd: ROOT, encoding: 'utf8', maxBuffer: 64 * 1024 * 1024 },
    )
  } catch (e) {
    if (e.status === 1) return []
    throw e
  }
  const hits = []
  for (const line of raw.split('\n')) {
    if (!line) continue
    const m = line.match(/^(.+?):(\d+):@(?:standard|rfc)\s+(.+)$/)
    if (m) hits.push({ path: m[1], value: m[3].trim() })
  }
  return hits
}

function sectionOf(value) {
  const m = value.match(/§[^\s,)]+|Art\.[^\s,)]+|BG-\d+|Annex\s+\w+/i)
  return m ? m[0] : ''
}

// Build the citation matcher for a registered standard: an explicit `match`
// override, else FAMILY[- ]?NUMBER, else the longest digit run, else the id.
function matcherFor(std) {
  if (std.match) return new RegExp(std.match, 'i')
  const fm = std.id.match(/^([A-Za-z/]+)-(\d+[A-Za-z]?)$/)
  if (fm) return new RegExp(fm[1].replace('/', '\\/') + '[- ]?' + fm[2] + '\\b', 'i')
  const big = (std.id.match(/\d{3,}/g) || []).sort((a, b) => b.length - a.length)[0]
  if (big) return new RegExp(big, 'i')
  return new RegExp('\\b' + std.id.replace(/[.*+?^${}()|[\]\\]/g, '\\$&') + '\\b', 'i')
}

// --- 3. join registry ⊕ banners -> the catalogue ---
function build() {
  const reg = registry()
  const hits = scan()
  const matchers = reg.map((s) => ({ std: s, re: matcherFor(s) }))
  const cat = reg.map((s) => ({ id: s.id, family: s.family, title: s.title, count: 0, modules: new Map() }))
  const catById = new Map(cat.map((e) => [e.id, e]))
  let matched = 0
  for (const h of hits) {
    let any = false
    for (const { std, re } of matchers) {
      if (re.test(h.value)) {
        const e = catById.get(std.id)
        e.count++
        if (!e.modules.has(h.path)) e.modules.set(h.path, sectionOf(h.value))
        any = true
      }
    }
    if (any) matched++
  }
  const entries = cat
    .map((e) => ({
      id: e.id,
      family: e.family,
      title: e.title,
      count: e.count,
      modules: [...e.modules.entries()].sort().slice(0, 24).map(([path, section]) => ({ path, section })),
    }))
    .sort((a, b) => a.family.localeCompare(b.family) || b.count - a.count || a.id.localeCompare(b.id))
  return { entries, totalHits: hits.length, matched }
}

// --- 4a. emit the shared catalogue TS (payload + vitepress both import facts) ---
function emitCatalogue(entries) {
  const body = `/**
 * GENERATED by scripts/standards-catalogue.mjs — do not edit by hand.
 *
 * The ONE shared index: the curated registry (src/standards/registry.ts) joined
 * to the live @standard / @rfc banners dissolved across src/. Consumed by BOTH
 * frontends — the payload seed (src/standards/seed.ts) and the vitepress page
 * (src/standards/SKILL.md). Logic meets here. Regenerate:
 *   node scripts/standards-catalogue.mjs     ·     verify: --verify
 *
 * @standard ISO/IEC-25010:2023 §5.4 reusability (one scan, two consumers)
 * @standard ISO-19011:2018 §6.4 audit-evidence (the citation index)
 * @rfc 9562 content-uuid (each standard row is content-addressed)
 */

export interface CatalogueEntry {
  /** Canonical standard id (the seed standardId + index key). */
  readonly id: string
  /** Payload \`standards.family\` enum value. */
  readonly family: string
  /** Curated canonical title. */
  readonly title: string
  /** Live banner-citation count across src/. */
  readonly count: number
  /** Distinct citing modules (repo-relative path + first section pin). */
  readonly modules: ReadonlyArray<{ readonly path: string; readonly section: string }>
}

export const STANDARDS_CATALOGUE: ReadonlyArray<CatalogueEntry> = ${JSON.stringify(entries, null, 2)} as const

export const STANDARDS_COUNT = ${entries.length}
`
  writeFileSync(CATALOGUE_TS, body)
}

// --- 4b. inject the rendered index into the vitepress-served SKILL.md page ---
function emitSkillSection(entries) {
  if (!existsSync(SKILL_MD)) return
  const START = '<!-- CATALOGUE:START -->'
  const END = '<!-- CATALOGUE:END -->'
  const cited = entries.filter((e) => e.count > 0)
  const byFam = new Map()
  for (const e of cited) { if (!byFam.has(e.family)) byFam.set(e.family, []); byFam.get(e.family).push(e) }
  const out = [START, '',
    `## Catalogue — ${cited.length} standards, ${cited.reduce((n, e) => n + e.count, 0)} citations`, '',
    '<!-- GENERATED from registry.ts ⊕ @standard banners by scripts/standards-catalogue.mjs. Do not edit by hand. -->',
    '', 'The standards erpax cites are not folders — they are dissolved across `src/` as `@standard` banners. This index is where they meet (the same data seeds the payload `standards` collection).', '']
  for (const fam of [...byFam.keys()].sort()) {
    out.push(`### ${fam}`, '')
    for (const e of byFam.get(fam)) out.push(`- \`${e.id}\` — ${e.title} · ${e.count}`)
    out.push('')
  }
  out.push(END)
  const block = out.join('\n')
  let md = readFileSync(SKILL_MD, 'utf8')
  md = md.includes(START) && md.includes(END)
    ? md.replace(new RegExp(START + '[\\s\\S]*?' + END), block)
    : md.replace(/\n*$/, '') + '\n\n' + block + '\n'
  writeFileSync(SKILL_MD, md)
}

const { entries, totalHits, matched } = build()
const cited = entries.filter((e) => e.count > 0)
const uncited = entries.filter((e) => e.count === 0)

if (process.argv.includes('--verify')) {
  const fresh = JSON.stringify(entries)
  const cur = existsSync(CATALOGUE_TS)
    ? (readFileSync(CATALOGUE_TS, 'utf8').match(/STANDARDS_CATALOGUE[^=]*=\s*([\s\S]*?)\s*as const/)?.[1] ?? '')
    : ''
  let curN = 'STALE'
  try { curN = JSON.stringify(JSON.parse(cur)) } catch { /* stale */ }
  if (curN !== fresh) { console.error('ERROR: src/standards/catalogue.ts is stale. Run: node scripts/standards-catalogue.mjs'); process.exit(1) }
  console.log(`OK — catalogue fresh (${cited.length} cited standards).`)
} else {
  emitCatalogue(entries)
  emitSkillSection(entries)
  console.log(`Wrote catalogue.ts + SKILL.md index.`)
  console.log(`  registry: ${entries.length} · cited: ${cited.length} · uncited: ${uncited.length}`)
  console.log(`  banners: ${totalHits} scanned, ${matched} matched to a registered standard (${(100 * matched / Math.max(1, totalHits)).toFixed(0)}% coverage)`)
  if (uncited.length) console.log(`  uncited registry rows (cite or trim): ${uncited.map((e) => e.id).join(', ')}`)
}
