#!/usr/bin/env node
// vocab-gen — compute the SHARED VOCABULARY and emit it as a committed, portable
// artifact (src/vocabulary/words.ts). A word is GROUNDED if it comes from a
// standard vocabulary: the English dictionary (∪ inflections), schema.org (the
// SKILL.md "schema.org component word" marker), a standard code (numeric / short
// abbreviation), or it composes from grounded roots. The grounded set is the
// vocabulary; anything left UNGROUNDED is reported (a genuine stray to fix).
//
//   node scripts/vocab-gen.mjs        # report ungrounded
//   node scripts/vocab-gen.mjs --emit # write src/vocabulary/words.ts
import { readFileSync, writeFileSync, readdirSync, statSync } from 'node:fs'
import { join, basename, dirname } from 'node:path'

const dict = new Set(
  readFileSync('/usr/share/dict/words', 'utf8').split('\n').map((w) => w.toLowerCase().trim()).filter(Boolean),
)
const SRC = join(process.cwd(), 'src')
const skillByAtom = new Map()
const walk = (d) => {
  for (const e of readdirSync(d)) {
    if (e === 'skills' || e === 'node_modules' || e.startsWith('.')) continue
    const p = join(d, e)
    let s
    try { s = statSync(p) } catch { continue }
    if (s.isDirectory()) walk(p)
    else if (e === 'SKILL.md') {
      const atom = basename(dirname(p)).toLowerCase().replace(/[-_]/g, '')
      if (!skillByAtom.has(atom)) skillByAtom.set(atom, readFileSync(p, 'utf8'))
    }
  }
}
walk(SRC)
const atoms = [...skillByAtom.keys()].sort()

const SUFFIX = [['ies', 'y'], ['ied', 'y'], ['ing', ''], ['ing', 'e'], ['ed', ''], ['ed', 'e'], ['es', ''], ['s', ''], ['d', ''], ['er', ''], ['er', 'e'], ['ly', '']]
const inDict = (w) => dict.has(w) || SUFFIX.some(([suf, rep]) => w.length > suf.length + 2 && w.endsWith(suf) && dict.has(w.slice(0, -suf.length) + rep))
const isCode = (w) => /\d/.test(w) || (w.length <= 5 && /^[a-z]+$/.test(w))
const isSchema = (w) => /schema\.org component word|Attested in schema\.org/.test(skillByAtom.get(w) || '')

// established domain / standard terms the 1990s wordlist lacks but are real vocabulary
const DOMAIN = new Set([
  'ebitda', 'ecommerce', 'edifact', 'vitepress', 'plugin', 'plugins', 'fractal', 'idempotency',
  'cardinality', 'permaculture', 'mycorrhizae', 'terroir', 'byline', 'bylines', 'forcemajeure',
  'upsell', 'coordinate', 'coordinates', 'cooperative', 'encodings', 'attendee', 'attendees',
  'followup', 'metadata', 'dataset', 'runtime', 'runbook', 'roadmap', 'changelog', 'walkthrough',
])

const grounded = new Set()
const ungrounded = []
const groundable = (w, seen = new Set()) => {
  if (grounded.has(w) || inDict(w) || isCode(w) || isSchema(w) || DOMAIN.has(w)) return true
  if (seen.has(w)) return false
  seen.add(w)
  for (let i = 3; i <= w.length - 3; i++) {
    const l = w.slice(0, i), r = w.slice(i)
    if ((inDict(l) || grounded.has(l) || atoms.includes(l)) && groundable(r, seen)) return true
  }
  return false
}
for (const a of atoms) {
  if (groundable(a)) grounded.add(a)
  else ungrounded.push(a)
}

console.log(`vocab-gen — ${atoms.length} atoms · grounded ${grounded.size} · UNGROUNDED ${ungrounded.length}`)
if (ungrounded.length) console.log('  ' + ungrounded.join(' '))

if (process.argv.includes('--emit')) {
  // the vocabulary = every IRREDUCIBLE token used to ground an atom (the atom
  // itself when it is a root, or each leaf of its longest-prefix decomposition —
  // so compound parts like exempt/well/being/zero that are NOT atoms are included).
  const isRoot = (w) => inDict(w) || isCode(w) || isSchema(w) || DOMAIN.has(w)
  const used = new Set()
  const collect = (w, seen = new Set()) => {
    if (isRoot(w)) { used.add(w); return true }
    if (seen.has(w)) return false
    seen.add(w)
    for (let i = w.length - 3; i >= 3; i--) {
      const l = w.slice(0, i)
      if (isRoot(l) && collect(w.slice(i), seen)) { used.add(l); return true }
    }
    return false
  }
  for (const a of atoms) collect(a)
  const roots = [...used].sort()
  const header = `/**\n * GENERATED by scripts/vocab-gen.mjs — do not edit by hand.\n *\n * THE SHARED VOCABULARY: the irreducible words erpax is allowed to use, each\n * grounded in a standard — the English dictionary, schema.org, a standard code,\n * or an established domain term. Every atom must be one of these or compose from\n * them ([[vocabulary]]). New words must extend this set deliberately. Regenerate:\n * node scripts/vocab-gen.mjs --emit\n */\n`
  writeFileSync(
    join(SRC, 'vocabulary', 'words.ts'),
    header + `export const SHARED_VOCABULARY: ReadonlySet<string> = new Set(${JSON.stringify(roots)})\n`,
  )
  console.log(`emitted src/vocabulary/words.ts — ${roots.length} roots`)
}
