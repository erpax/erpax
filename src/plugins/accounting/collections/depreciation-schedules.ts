/**
 * # Depreciation Schedules Collection
 *
 * @summary Period-by-period depreciation posting evidence per IAS-16 §68-76 for each fixed asset, with GL entry reference.
 *
 * ## Core Function
 *
 * The Depreciation Schedules collection maintains the detailed, period-by-period audit trail of depreciation
 * posted against each asset in the Fixed Assets register. Where the fixed-assets collection holds the asset master
 * (cost, useful life, depreciation method), this collection records the actual depreciation expense recognized
 * each fiscal period, the carrying amounts before and after, and the corresponding GL posting reference.
 * Each row is immutable once status = 'posted' (GL entry booked); reversal entries go into separate rows with
 * status = 'reversed'. The collection supports multiple depreciation methods (straight-line, declining-balance,
 * units-of-activity, etc.) and is the primary SOX §404 audit evidence that depreciation was calculated per
 * policy and GL postings tie to the fixed-assets register. Period lock (validateNotLocked hook) prevents
 * modification of closed periods.
 *
 * ## Architecture
 *
 * Depreciation schedules are auto-generated by a scheduled job (or triggered manually) based on the asset master
 * and the fiscal calendar. Multi-tenancy is enforced via the `tenant` field. Each row links to exactly one fixed asset
 * (relationTo: 'fixed-assets', required, indexed) and one fiscal period (implicit in periodEnd date; optionally
 * linked via 'fiscal-periods' relationship). The collection respects period-lock constraints: modification is prevented
 * once the period transitions to 'closed' or 'locked' status per validateNotLocked utility. Status lifecycle: 'calculated'
 * (initial creation by job) → 'posted' (GL entry booked; fires afterChange hook that calls depreciationSchedulePostingHook
 * to emit the GL entry to journalEntryService) → 'reversed' (reversal JE booked if needed). The depreciation method field
 * captures the calculation method applied (must match the asset's depreciationMethod for auditability). Accumulated and
 * book-value snapshots (accumulatedAfter, bookValueAfter) are read-only and computed at posting time, providing
 * an immutable carry-forward for the next period's opening balance. GL account pointers (interestExpenseAccount,
 * leaseLiabilityAccount) are optional overrides; if not specified, the system uses the asset's default accounts.
 *
 * ## Hooks & Validation
 *
 * - **beforeValidate:**
 *   - `autoPopulateTenant`: Stamps `tenant` from context.
 * - **beforeChange:**
 *   - `validateNotLocked`: Prevents modification if the fiscal period is closed/locked per period-lock policy.
 *   - `autoPopulateCreatedBy`: Stamps `createdBy` on initial insert.
 *   - `autoSetTimestamp('postedAt', ...)`: Auto-stamps `postedAt` with ISO-8601 timestamp when status → 'posted'.
 * - **afterChange:**
 *   - `depreciationSchedulePostingHook`: Fires when status → 'posted'; emits 'depreciation:posted' event to glPostingService,
 *     which books Dr Depreciation Expense / Cr Accumulated Depreciation GL entries per the asset's GL-account mappings.
 *   - `auditTrailAfterChange('depreciation-schedules')`: Logs full record delta to audit-events for compliance.
 *
 * ## Key Fields
 *
 * - **scheduleId (text, required, unique, indexed):** Stable identifier for the schedule row (e.g., 'DEPR-2026-04-ASSET-001'). Index for reporting queries.
 * - **fixedAsset (relationship to fixed-assets, required, indexed):** The asset being depreciated. Immutable once created; links to assetNumber for user clarity.
 * - **periodEnd (date, required, indexed):** Fiscal period end date (last day of month, quarter, or year). Index for period-by-period audit queries and balance reconciliation.
 * - **periodStart (date, required):** Fiscal period start date (first day of month, quarter, or year). Used to calculate accrual length.
 * - **depreciationAmount (number, required, min 0):** Depreciation expense for this period (cents). Calculated per the asset's depreciationMethod: straight-line = depreciableBase / usefulLifeYears / periods_per_year; declining-balance = openingBookValue × declineRate; units-of-activity = (unitsProducedThisPeriod / totalUnitsExpected) × depreciableBase.
 * - **accumulatedAfter (number, read-only):** Cumulative depreciation immediately after this posting (cents). = accumulatedBefore + depreciationAmount. Provides an immutable carry-forward snapshot for audit verification.
 * - **bookValueAfter (number, read-only):** Net book value immediately after this posting (cents). = assetCost − accumulatedAfter per IAS-16 §68-70. Auditable evidence for balance-sheet reporting.
 * - **currency (select, default 'EUR'):** Currency of depreciation amounts; must match the asset's currency per ISO-4217:2015.
 * - **method (select):** Depreciation method applied this period: straight_line, declining_balance, double_declining, units_of_activity, sum_of_years_digits. Must match the asset's depreciationMethod for consistency checks.
 * - **status (select, default 'calculated'):** Lifecycle state: 'calculated' (initial creation, ready for posting), 'posted' (GL entry booked; immutable), 'reversed' (reversal entry booked, immutable).
 * - **postedAt (date, read-only):** ISO-8601 timestamp when status → 'posted'; auto-stamped by autoSetTimestamp hook. Null until posting.
 * - **journalEntry (relationship to journal-entries, read-only):** Reference to the GL posting created by depreciationSchedulePostingHook. Null until status = 'posted'.
 * - **notes (textarea):** Optional commentary (e.g., "Asset fully depreciated next period", "Useful life revised in period 2026-06").
 * - **createdBy (user ID):** User who created the schedule row (usually the scheduled job, or manual accountant entry).
 * - **createdAt (ISO-8601 timestamp):** Schedule row creation timestamp.
 * - **modifiedBy (user ID, read-only after posting):** Last modifier; locked once status = 'posted'.
 * - **modifiedAt (ISO-8601 timestamp, read-only after posting):** Last modification timestamp.
 *
 * ## Core Invariants
 *
 * - **Immutability Post-Posting:** Once status = 'posted' and journalEntry is set, the depreciation-schedule row is immutable (cannot update depreciationAmount, accumulatedAfter, bookValueAfter, or method). Any corrections require a reversal entry + new schedule row.
 * - **Period Lock Enforcement:** The validateNotLocked hook prevents creation or modification of rows in closed/locked fiscal periods per SOX §404 requirement that posted periods are non-editable.
 * - **Depreciation Method Consistency:** The method field must always match the asset's depreciationMethod at the time of posting. If the asset changes methods (per useful-life restatement per IAS-8), new schedules use the new method; prior-period schedules remain locked with their original method.
 * - **GL Account Immutability:** depreciationExpenseAccount and accumulatedDepreciationAccount referenced from the asset are immutable; they cannot be changed mid-period.
 * - **Accumulated Depreciation Monotonicity:** accumulatedAfter must always be ≥ the prior period's accumulatedAfter (non-decreasing), except in reversal rows where accumulatedAfter = prior − reversal amount.
 * - **Book Value Non-Negativity:** bookValueAfter must never be negative and must never be less than the asset's residualValue. Once bookValue = residualValue, status auto-transitions to 'fully_depreciated' on the asset.
 *
 * ## Audit Trail
 *
 * Every depreciation-schedule record captures:
 * - `createdBy` (user ID + timestamp): Schedule row creation stamp (typically a scheduled job's service account).
 * - `modifiedBy` (user ID + timestamp, locked once posted): Last modifier ID for access-control audit.
 * - All changes prior to posting are logged to audit-events with full before/after values.
 * Once status = 'posted', the row is immutable and the GL posting is recorded in the journal-entries collection.
 * Reversals (if needed) create separate rows with status = 'reversed' and a reference to the original journalEntry for traceability.
 * Per SOX §404, auditors can walk from a fixed-assets register row → depreciation-schedules rows for the asset →
 * journal-entries for each month to reconstruct the full GL impact of depreciation and verify completeness and accuracy.
 * @standard SOX §302 §404 internal-controls capital-asset-control; @standard IAS-16 §68-76 depreciation-disclosure-guidance.
 *
 * ## Example
 *
 * ```javascript
 * {
 *   "_id": "depr_sched_uuid_001",
 *   "tenant": "tenant_bg_001",
 *   "scheduleId": "DEPR-2026-04-FIX-0042",
 *   "fixedAsset": "fa_uuid_machinery_001",
 *   "periodStart": "2026-04-01",
 *   "periodEnd": "2026-04-30",
 *   "depreciationAmount": 75000,  // 750 BGN = 9,000 BGN/year ÷ 12 months
 *   "accumulatedBefore": 2625000,  // 26,250 BGN (prior 35 months)
 *   "accumulatedAfter": 2700000,  // 27,000 BGN (add 750 BGN this month)
 *   "bookValueAfter": 2300000,  // 50,000 − 27,000 BGN
 *   "currency": "BGN",
 *   "method": "straight_line",
 *   "status": "posted",
 *   "postedAt": "2026-04-30T18:45:00Z",
 *   "journalEntry": "je_uuid_depr_042",
 *   "createdBy": "batch_job_depreciation",
 *   "createdAt": "2026-04-30T16:00:00Z"
 * }
 * ```
 *
 * @phase 2.12 JSDoc-as-spec implementation for depreciation schedules
 * @useCase Depreciation posting, period-end close, SOX §404 audit evidence, balance-sheet PP&E reporting
 * @accounting IAS-16 §52-67 depreciation-accounting, §68-76 disclosure-guidance
 * @accounting IAS-36 §117-120 impairment-loss-reversal
 * @accounting US-GAAP ASC-360-10-35 depreciation-methods-and-changes
 * @standard ISO-4217:2015 currency-codes
 * @standard ISO-8601-1:2019 date-time fiscal-period-boundaries
 * @audit ISO-19011:2018 audit-trail period-evidence-immutability
 * @compliance SOX §302 §404 internal-controls period-lock-enforcement
 * @security ISO-27001 A.5.23 cloud-service-tenant-isolation
 * @see src/plugins/accounting/collections/fixedassets.ts (asset master)
 * @see src/hooks/collections/accounting/depreciation.hook.ts (posting hook)
 * @see docs/STANDARDS.md §4.2 depreciation-accounting
 */

import type { CollectionConfig } from 'payload'
import { autoPopulateTenant } from '@/hooks/autoPopulateTenant'
import { autoPopulateCreatedBy } from '@/hooks/autoPopulateCreatedBy'
import { autoSetTimestamp } from '@/hooks/autoSetTimestamp'
import { auditTrailAfterChange } from '@/hooks/auditTrailAfterChange'
import { roleScopedAccess, scopedAccess, tenantAdmin } from '@/access/auth'
import { multiTenancyField, currencyField, statusField, notesField, auditFields } from '@/fields/accounting/base-accounting-fields'
import { validateNotLocked } from '@/services/accounting/utilities/period-lock'
import { depreciationSchedulePostingHook } from '@/hooks/collections/accounting/depreciation.hook'

const DepreciationSchedules: CollectionConfig = {
  slug: 'depreciation-schedules',
  labels: { singular: 'Depreciation Schedule', plural: 'Depreciation Schedules' },
  admin: { useAsTitle: 'scheduleId', defaultColumns: ['fixedAsset', 'periodEnd', 'depreciationAmount', 'status'] },
  access: {
    read: scopedAccess(),
    create: roleScopedAccess('admin', 'accountant'),
    update: roleScopedAccess('admin', 'accountant'),
    delete: tenantAdmin,
  },
  fields: [
    multiTenancyField(),
    { name: 'scheduleId', type: 'text', required: true, unique: true, index: true },
    { name: 'fixedAsset', type: 'relationship', relationTo: 'fixed-assets', required: true, index: true },
    { name: 'periodEnd', type: 'date', required: true, index: true },
    { name: 'periodStart', type: 'date', required: true },
    { name: 'depreciationAmount', type: 'number', required: true, min: 0 },
    { name: 'accumulatedAfter', type: 'number', admin: { readOnly: true, description: 'Accumulated depreciation after this entry (cents).' } },
    { name: 'bookValueAfter', type: 'number', admin: { readOnly: true, description: 'Book value after this entry (cents).' } },
    currencyField(),
    {
      name: 'method',
      type: 'select',
      options: [
        { label: 'Straight Line', value: 'straight_line' },
        { label: 'Declining Balance', value: 'declining_balance' },
        { label: 'Double Declining', value: 'double_declining' },
        { label: 'Units of Activity', value: 'units_of_activity' },
        { label: 'Sum of Years Digits', value: 'sum_of_years_digits' },
      ],
    },
    statusField(
      [
        { label: 'Calculated', value: 'calculated' },
        { label: 'Posted', value: 'posted' },
        { label: 'Reversed', value: 'reversed' },
      ],
      'calculated',
    ),
    { name: 'postedAt', type: 'date', admin: { readOnly: true } },
    { name: 'journalEntry', type: 'relationship', relationTo: 'journal-entries', admin: { readOnly: true } },
    ...auditFields(),
    notesField(),
  ],
  hooks: {
    beforeValidate: [autoPopulateTenant],
    beforeChange: [
      validateNotLocked,
      autoPopulateCreatedBy,
      autoSetTimestamp('postedAt', (d) => (d as { status?: string }).status === 'posted'),
    ],
    afterChange: [
      // GL post when status flips to 'posted' — emits depreciation:posted →
      // glPostingService books Dr Depreciation Expense / Cr Accumulated Depreciation.
      depreciationSchedulePostingHook,
      auditTrailAfterChange('depreciation-schedules'),
    ],
  },
  timestamps: true,
}

export default DepreciationSchedules
