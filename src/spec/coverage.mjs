#!/usr/bin/env node
/**
 * Seed → test coverage reporter — prints the matrix without failing.
 *
 * Companion to `tests/standards/coverage/seed-test-coverage.int.spec.ts`
 * (the auto-failing version). Use this for human review when the test
 * surfaces a coverage gap or redundancy and you want to see the full
 * picture before deciding which spec to update.
 *
 * Usage:
 *   node src/spec/coverage.mjs
 *
 * Output: a markdown table of (collection × spec-files-pinning-it) plus a
 * list of redundancies and a list of uncited spec files. Designed to be
 * piped into a markdown viewer or a GitHub issue.
 *
 * @standard ISO/IEC-29119:2022 software-testing test-meta-coverage
 * @audit ISO-19011:2018 audit-trail seed-test-traceability
 * @compliance SOX §404 internal-controls control-coverage-matrix
 */

import { readFileSync, readdirSync, statSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join, resolve } from 'node:path'

const __filename = fileURLToPath(import.meta.url)
const REPO_ROOT = resolve(dirname(__filename), '..')
const TESTS_ROOT = resolve(REPO_ROOT, 'tests')
const REGISTRY_FILE = resolve(REPO_ROOT, 'src/testing/test-seed-factory.ts')

// ─── Parse SEED_VALIDATION_REGISTRY at lexer level (no TS execution needed)
//
// Pulls every `<slug>: { label: '<label>', requiredFields: [...] }` block.
// Lexer-only so we can run from plain node without tsx.

const registrySource = readFileSync(REGISTRY_FILE, 'utf8')
const registryMatch = registrySource.match(
  /SEED_VALIDATION_REGISTRY[\s\S]*?=\s*\{([\s\S]*?)^\}/m,
)
if (!registryMatch) {
  console.error('Could not locate SEED_VALIDATION_REGISTRY in test-seed-factory.ts')
  process.exit(2)
}
const body = registryMatch[1]
const collections = []
const collectionRe = /['"]?([\w-]+)['"]?:\s*\{[\s\S]*?label:\s*['"]([^'"]+)['"][\s\S]*?requiredFields:\s*\[([^\]]+)\]/g
for (const m of body.matchAll(collectionRe)) {
  const requiredFields = m[3]
    .split(',')
    .map((s) => s.trim().replace(/['"`]/g, ''))
    .filter(Boolean)
  collections.push({ slug: m[1], label: m[2], requiredFields })
}

// ─── Walk every *.int.spec.ts(x) under tests/

const walk = (dir, out = []) => {
  for (const entry of readdirSync(dir)) {
    const abs = join(dir, entry)
    const stat = statSync(abs)
    if (stat.isDirectory()) walk(abs, out)
    else if (/\.int\.spec\.tsx?$/.test(entry)) out.push(abs)
  }
  return out
}

const specs = walk(TESTS_ROOT)
  .filter((p) => !p.endsWith('seed-test-coverage.int.spec.ts'))
  .map((abs) => ({
    path: abs.slice(REPO_ROOT.length + 1),
    source: readFileSync(abs, 'utf8'),
  }))

// ─── Report ─────────────────────────────────────────────────────────────

const escapeRegex = (s) => s.replace(/[.*+?^${}()|[\]\\]/g, '\\$&')
const matchingSpecsFor = (label) => {
  const re = new RegExp(`['"\`]${escapeRegex(label)}: \\w+ is required['"\`]`)
  return specs.filter((f) => re.test(f.source)).map((f) => f.path)
}

console.log('# Seed → test coverage matrix')
console.log()
console.log('Generated by `src/spec/coverage.mjs`. Companion to')
console.log('`tests/standards/coverage/seed-test-coverage.int.spec.ts` (auto-failing).')
console.log()
console.log('## Collections registered in `SEED_VALIDATION_REGISTRY`')
console.log()
console.log('| Collection | Label | Required fields | Specs pinning the contract |')
console.log('|---|---|---|---|')

let coverageGapCount = 0
for (const c of collections) {
  const specsPinning = matchingSpecsFor(c.label)
  if (specsPinning.length === 0) coverageGapCount += 1
  const specsCol =
    specsPinning.length === 0
      ? '⚠️ **NONE** — coverage gap'
      : specsPinning.map((p) => `\`${p}\``).join('<br>')
  console.log(
    `| \`${c.slug}\` | ${c.label} | ${c.requiredFields.length} fields | ${specsCol} |`,
  )
}
console.log()
console.log(`Total collections: **${collections.length}**`)
console.log(`Coverage gaps: **${coverageGapCount}**`)

// ─── Redundancy report ─────────────────────────────────────────────────

const re = /['"`](([A-Z][A-Za-z ]+)): (\w+) is required['"`]/g
const occurrences = new Map()
for (const spec of specs) {
  const seen = new Set()
  for (const m of spec.source.matchAll(re)) {
    const fingerprint = `${m[1]}: ${m[3]}`
    if (seen.has(fingerprint)) continue
    seen.add(fingerprint)
    const list = occurrences.get(fingerprint) ?? []
    list.push(spec.path)
    occurrences.set(fingerprint, list)
  }
}
const duplicated = [...occurrences.entries()].filter(([, files]) => files.length > 1)

console.log()
console.log('## Redundancy detector')
console.log()
if (duplicated.length === 0) {
  console.log('✅ No duplicated `<Label>: <field>` assertions across specs.')
} else {
  console.log('⚠️  Duplicated assertions (drift latency — pick one spec to own each):')
  console.log()
  for (const [fp, files] of duplicated) {
    console.log(`- \`${fp}\` pinned in:`)
    for (const f of files) console.log(`    - \`${f}\``)
  }
}

// ─── Citation gap report ───────────────────────────────────────────────

const uncited = specs.filter(
  (f) => !/@(standard|compliance|audit|accounting|security|rfc)\b/.test(f.source.slice(0, 1500)),
)
console.log()
console.log('## Citation banner gaps')
console.log()
if (uncited.length === 0) {
  console.log('✅ Every spec carries a `@standard` / `@compliance` / `@audit` / `@accounting` / `@security` / `@rfc` citation in its first 1500 chars.')
} else {
  console.log('⚠️  Spec files missing a JSDoc citation banner:')
  console.log()
  for (const u of uncited) console.log(`- \`${u.path}\``)
}

const hasDrift = coverageGapCount > 0 || duplicated.length > 0 || uncited.length > 0
process.exit(hasDrift ? 1 : 0)
