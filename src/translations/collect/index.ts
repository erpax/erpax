/**
 * translations/collect — THE COLLECTOR (computed in the skill, not in scripts/).
 *
 * Ports every atom's SKILL.md into code: "whatever the SKILL.md says,
 * computationally ported". For each `src/<path>/SKILL.md` it extracts the
 * translatable messages (`name`, `description`), runs each through the model
 * (../translation#defineTranslation → words + messaging-uuid + en-source), and:
 *
 *   - writes a MASSLESS `<folder>/translations.ts` (pure data, `import type`
 *     only — no runtime import of the matrix), the per-folder projection, and
 *   - emits the ONE canonical `src/translations/catalogue.ts` (the mass) that
 *     the coverage test + payload seed + vitepress read.
 *
 * The gravity (the compute) lives HERE; the per-folder files are shadows of it,
 * regenerable + `--verify`-gated, never hand-edited. Flatten · DRY · keep the
 * gravity.
 *
 * The resolver (norm = lowercase, strip [-_]; skip symlinks) is the SAME one
 * aura/scan.mjs ∥ uuid/matrix/collide.mjs ∥ .vitepress/corpus.mts use, so a word
 * that resolves here resolves there: translation coverage ≡ aura coverage.
 *
 *   pnpm translations:collect            write all per-folder files + catalogue
 *   pnpm translations:collect --dry      compute only; print coverage, write nothing
 *   pnpm translations:collect --limit N  write the first N folders (proof slice)
 *   pnpm translations  (--verify)        gate: fail if any projection is stale
 *
 * @standard BCP-47 language tags · RFC 9562 §5.8 content-uuid (messaging-uuid)
 * @audit aura gap parity — a non-atom word here is a mint-queue word there
 * @see ../translation (model) · ../message (messaging-uuid) · ../aura (the gap)
 */
import { readdirSync, readFileSync, writeFileSync, existsSync } from 'node:fs'
import { join, dirname, basename } from 'node:path'
import { isRealDir } from '@/aura'
import { defineTranslation, type Translation } from '@/translation'
import { isAtomWord, splitWords } from '@/message'

const ROOT = join(process.cwd(), 'src')
const argv = process.argv.slice(2)
const DRY = argv.includes('--dry')
const VERIFY = argv.includes('--verify')
const LIMIT = (() => {
  const i = argv.indexOf('--limit')
  return i >= 0 && argv[i + 1] ? Math.max(0, parseInt(argv[i + 1]!, 10)) : Infinity
})()

// ── the shared resolver: walk SKILL.md, skip symlinks (the src/skills → . loop) ──
function walk(dir: string, out: string[] = []): string[] {
  for (const name of readdirSync(dir).sort()) {
    if (name === 'node_modules' || name.startsWith('.')) continue
    const full = join(dir, name)
    if (!isRealDir(full)) continue
    if (existsSync(join(full, 'SKILL.md'))) out.push(join(full, 'SKILL.md'))
    walk(full, out)
  }
  return out
}

/** Frontmatter scalar (quoted or bare) — mirrors skill/router/build#fmValue. */
function fmValue(fm: string, key: string): string {
  const m = fm.match(new RegExp(`^${key}:\\s*(.+)$`, 'm'))
  if (!m) return ''
  let v = m[1]!.trim()
  if ((v.startsWith('"') && v.endsWith('"')) || (v.startsWith("'") && v.endsWith("'"))) v = v.slice(1, -1)
  return v
}

interface AtomTranslations {
  /** The atom's leaf word (basename of its folder). */
  readonly atom: string
  /** Repo-relative folder path (src-relative), the route key. */
  readonly path: string
  readonly translations: readonly Translation[]
}

/** Port one SKILL.md → its translatable messages (name + description). */
function collectAtom(skillPath: string): AtomTranslations {
  const raw = readFileSync(skillPath, 'utf8')
  const fmMatch = raw.match(/^---\n([\s\S]*?)\n---\n?/)
  const fm = fmMatch ? fmMatch[1]! : ''
  const folder = dirname(skillPath)
  const atom = basename(folder)
  const path = folder.slice(ROOT.length + 1)
  const messages: [string, string][] = [
    ['name', fmValue(fm, 'name') || atom],
    ['description', fmValue(fm, 'description')],
  ].filter(([, v]) => v.length > 0) as [string, string][]
  return { atom, path, translations: messages.map(([k, v]) => defineTranslation(k, v)) }
}

// ── emit ──
const PER_FOLDER_BANNER =
  '// AUTO-GENERATED by @/translations collect — do not edit.\n' +
  '// Massless projection of the dense core (../translation model + ../message uuid).\n' +
  '// Regenerate: pnpm translations:collect   ·   verify: pnpm translations\n'

function perFolderFile(t: AtomTranslations): string {
  return (
    PER_FOLDER_BANNER +
    "import type { Translation } from '@/translation'\n\n" +
    `export const translations: readonly Translation[] = ${JSON.stringify(t.translations, null, 2)}\n\n` +
    'export default translations\n'
  )
}

function catalogueFile(atoms: AtomTranslations[]): string {
  const entries = atoms.map((a) => ({ atom: a.atom, path: a.path, translations: a.translations }))
  return (
    '// AUTO-GENERATED by @/translations collect — do not edit. The ONE canonical index (the mass).\n' +
    '// Every atom\'s ported SKILL.md, content-addressed by the messaging-uuid. Regenerate: pnpm translations:collect\n' +
    "import type { Translation } from '@/translation'\n\n" +
    'export interface CatalogueEntry {\n' +
    '  readonly atom: string\n' +
    '  readonly path: string\n' +
    '  readonly translations: readonly Translation[]\n' +
    '}\n\n' +
    `export const TRANSLATIONS_CATALOGUE: readonly CatalogueEntry[] = ${JSON.stringify(entries, null, 2)}\n\n` +
    `export const TRANSLATIONS_COUNT = ${entries.length}\n`
  )
}

// ── run ──
const skills = walk(ROOT)
const atoms = skills.map(collectAtom)
const selected = atoms.slice(0, LIMIT === Infinity ? atoms.length : LIMIT)

// coverage / aura parity: across every message word, how many resolve to an atom?
const allWords = atoms.flatMap((a) => a.translations.flatMap((t) => splitWords(t.source)))
const resolved = allWords.filter(isAtomWord).length
const mintQueue = [...new Set(allWords.filter((w) => !isAtomWord(w)))].sort()
const coverage = allWords.length ? ((100 * resolved) / allWords.length).toFixed(1) : '0.0'

if (VERIFY) {
  let stale = 0
  for (const a of atoms) {
    const file = join(ROOT, a.path, 'translations.ts')
    const want = perFolderFile(a)
    const have = existsSync(file) ? readFileSync(file, 'utf8') : ''
    if (have !== want) {
      stale++
      if (stale <= 10) console.error(`STALE  ${a.path}/translations.ts`)
    }
  }
  const catFile = join(ROOT, 'translations', 'catalogue.ts')
  const catWant = catalogueFile(atoms)
  const catStale = !existsSync(catFile) || readFileSync(catFile, 'utf8') !== catWant
  if (catStale) console.error('STALE  translations/catalogue.ts')
  if (stale || catStale) {
    console.error(`\nERROR: ${stale} folder projection(s)${catStale ? ' + catalogue' : ''} stale. Run: pnpm translations:collect`)
    process.exit(1)
  }
  console.log(`OK — ${atoms.length} folder projections + catalogue fresh. coverage ${coverage}% (${mintQueue.length} mint-queue words).`)
} else if (DRY) {
  console.log(`translations (dry): ${atoms.length} atoms, ${atoms.reduce((n, a) => n + a.translations.length, 0)} messages.`)
  console.log(`word→atom coverage: ${coverage}%  (${resolved}/${allWords.length} word-occurrences resolve; ${mintQueue.length} distinct mint-queue words)`)
  console.log(`mint-queue sample: ${mintQueue.slice(0, 30).join(' ')}${mintQueue.length > 30 ? ' …' : ''}`)
  const sample = selected.find((a) => a.translations.length >= 2) ?? selected[0]
  if (sample) {
    console.log(`\n--- sample: src/${sample.path}/translations.ts ---`)
    console.log(perFolderFile(sample))
  }
} else {
  for (const a of selected) writeFileSync(join(ROOT, a.path, 'translations.ts'), perFolderFile(a))
  // The catalogue always reflects the FULL corpus (the mass), even on a --limit slice.
  writeFileSync(join(ROOT, 'translations', 'catalogue.ts'), catalogueFile(atoms))
  console.log(`translations: wrote ${selected.length} folder projection(s) + catalogue (${atoms.length} atoms). coverage ${coverage}%.`)
}
