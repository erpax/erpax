'use client'

import type { PayloadAdminBarProps, PayloadMeUser } from '@payloadcms/admin-bar'

import { cn } from '@/utilities/ui'
import { useSelectedLayoutSegments } from 'next/navigation'
import { PayloadAdminBar } from '@payloadcms/admin-bar'
import React, { useState } from 'react'
import { useRouter } from 'next/navigation'

import './index.scss'

import { getClientSideURL } from '@/utilities/getURL'

const baseClass = 'admin-bar'

const collectionLabels = {
  pages: {
    plural: 'Pages',
    singular: 'Page',
  },
  posts: {
    plural: 'Posts',
    singular: 'Post',
  },
  projects: {
    plural: 'Projects',
    singular: 'Project',
  },
}

type AdminBarLabels = {
  dashboard: string
  page: string
  pages: string
  post: string
  posts: string
  project: string
  projects: string
}

const Title: React.FC<{ dashboardLabel: string }> = ({ dashboardLabel }) => (
  <span>{dashboardLabel}</span>
)

export const AdminBar: React.FC<{
  adminBarProps?: PayloadAdminBarProps
  labels?: AdminBarLabels
}> = (props) => {
  const { adminBarProps, labels } = props || {}
  const segments = useSelectedLayoutSegments()
  const [show, setShow] = useState(false)
  const collection = (
    collectionLabels[segments?.[1] as keyof typeof collectionLabels] ? segments[1] : 'pages'
  ) as keyof typeof collectionLabels
  const router = useRouter()

  const onAuthChange = React.useCallback((user: PayloadMeUser) => {
    setShow(Boolean(user?.id))
  }, [])

  return (
    <div
      className={cn(baseClass, 'py-2 bg-black text-white', {
        block: show,
        hidden: !show,
      })}
    >
      <div className="container">
        <PayloadAdminBar
          {...adminBarProps}
          className="py-2 text-white"
          classNames={{
            controls: 'font-medium text-white',
            logo: 'text-white',
            user: 'text-white',
          }}
          cmsURL={getClientSideURL()}
          collectionSlug={collection}
          collectionLabels={{
            plural:
              labels?.[collectionLabels[collection]?.plural.toLowerCase() as keyof AdminBarLabels] ||
              collectionLabels[collection]?.plural ||
              'Pages',
            singular:
              labels?.[collectionLabels[collection]?.singular.toLowerCase() as keyof AdminBarLabels] ||
              collectionLabels[collection]?.singular ||
              'Page',
          }}
          logo={<Title dashboardLabel={labels?.dashboard || 'Dashboard'} />}
          onAuthChange={onAuthChange}
          onPreviewExit={() => {
            fetch('/next/exit-preview').then(() => {
              router.push('/')
              router.refresh()
            })
          }}
          style={{
            backgroundColor: 'transparent',
            padding: 0,
            position: 'relative',
            zIndex: 'unset',
          }}
        />
      </div>
    </div>
  )
}
