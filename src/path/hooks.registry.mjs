#!/usr/bin/env node
/**
 * path/hooks.registry.mjs — emit ATOM_LEDGER_PATHS + MERGED_LEDGER_CHAINS from index.ts walk.
 *
 *   node src/path/hooks.registry.mjs --emit
 *   pnpm path:hooks
 */
import { readdirSync, statSync, writeFileSync, existsSync } from 'node:fs'
import { join, relative, dirname } from 'node:path'

const SRC = join(process.cwd(), 'src')
const SKIP = new Set(['app', 'migrations'])
const OUT = join(SRC, 'path', 'hooks.registry.generated.ts')

const walkIndexPaths = (dir, rel = '') => {
  const out = []
  for (const e of readdirSync(dir)) {
    const p = join(dir, e)
    const r = rel ? `${rel}/${e}` : e
    if (!statSync(p).isDirectory()) continue
    if (SKIP.has(e)) continue
    if (existsSync(join(p, 'index.ts'))) out.push(r)
    out.push(...walkIndexPaths(p, r))
  }
  return out
}

const ancestorPaths = (atomPath) => {
  const parts = atomPath.split('/').filter(Boolean)
  return parts.map((_, i) => parts.slice(0, i + 1).join('/'))
}

const indexBearingParent = (child, set) => {
  const parts = child.split('/').filter(Boolean)
  for (let i = parts.length - 1; i >= 1; i--) {
    const prefix = parts.slice(0, i).join('/')
    if (set.has(prefix)) return prefix
  }
  return null
}

const indexBearingChain = (child, set) => ancestorPaths(child).filter((p) => set.has(p))

const buildMergedChains = (paths) => {
  const set = new Set(paths)
  const chains = {}
  for (const p of paths) {
    chains[p] = indexBearingChain(p, set)
  }
  return chains
}

const emit = () => {
  const paths = [...new Set(walkIndexPaths(SRC))].sort()
  const merged = buildMergedChains(paths)
  const nestedWithParent = paths.filter((p) => p.includes('/') && indexBearingParent(p, new Set(paths)))
  const body = `/**
 * AUTO-GENERATED by src/path/hooks.registry.mjs — do not edit.
 * Every atom barrel (index.ts) is ledger-eligible via @/path/hub recordAtomOnPath.
 * MERGED_LEDGER_CHAINS — index-bearing ancestor chain per path (parent hooks before child).
 * Re-run: pnpm path:hooks
 */
export const ATOM_LEDGER_PATHS: readonly string[] = ${JSON.stringify(paths, null, 2)} as const

export const ATOM_LEDGER_PATH_COUNT = ${paths.length} as const

/** Index-bearing prefix chain per atom path — parent barrels precede child. */
export const MERGED_LEDGER_CHAINS: Readonly<Record<string, readonly string[]>> = ${JSON.stringify(merged, null, 2)} as const

export const MERGED_NESTED_PATH_COUNT = ${nestedWithParent.length} as const
`
  writeFileSync(OUT, body)
  console.log(
    `path:hooks — ${paths.length} atom ledger paths · ${nestedWithParent.length} merged nested → ${relative(process.cwd(), OUT)}`,
  )
}

if (process.argv.includes('--emit')) emit()
