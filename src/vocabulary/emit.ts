/**
 * vocabulary/emit — compute the SHARED VOCABULARY and emit words.ts.
 *
 *   tsx src/vocabulary/emit.ts           # report ungrounded
 *   tsx src/vocabulary/emit.ts --emit    # write src/vocabulary/words.ts
 */
import { readFileSync, writeFileSync, readdirSync, statSync, existsSync } from 'node:fs'
import { join, basename, dirname } from 'node:path'

const DICT_PATH = '/usr/share/dict/words'

const SUFFIX: readonly (readonly [string, string])[] = [
  ['ies', 'y'],
  ['ied', 'y'],
  ['ing', ''],
  ['ing', 'e'],
  ['ed', ''],
  ['ed', 'e'],
  ['es', ''],
  ['s', ''],
  ['d', ''],
  ['er', ''],
  ['er', 'e'],
  ['ly', ''],
]

const DOMAIN = new Set([
  'ebitda', 'ecommerce', 'edifact', 'vitepress', 'plugin', 'plugins', 'fractal', 'idempotency',
  'cardinality', 'permaculture', 'mycorrhizae', 'terroir', 'byline', 'bylines', 'forcemajeure',
  'upsell', 'coordinate', 'coordinates', 'cooperative', 'encodings', 'attendee', 'attendees',
  'followup', 'metadata', 'dataset', 'runtime', 'runbook', 'roadmap', 'changelog', 'walkthrough',
])

function loadDict(): Set<string> {
  if (!existsSync(DICT_PATH)) return new Set()
  return new Set(
    readFileSync(DICT_PATH, 'utf8')
      .split('\n')
      .map((w) => w.toLowerCase().trim())
      .filter(Boolean),
  )
}

function walkSkillByAtom(src: string, skillByAtom: Map<string, string>): void {
  const walk = (d: string): void => {
    for (const e of readdirSync(d)) {
      if (e === 'skills' || e === 'node_modules' || e.startsWith('.')) continue
      const p = join(d, e)
      let s
      try {
        s = statSync(p)
      } catch {
        continue
      }
      if (s.isDirectory()) walk(p)
      else if (e === 'SKILL.md') {
        const atom = basename(dirname(p)).toLowerCase().replace(/[-_]/g, '')
        if (!skillByAtom.has(atom)) skillByAtom.set(atom, readFileSync(p, 'utf8'))
      }
    }
  }
  walk(src)
}

export interface VocabGenResult {
  atoms: string[]
  grounded: Set<string>
  ungrounded: string[]
}

export function computeVocabulary(cwd: string = process.cwd()): VocabGenResult {
  const dict = loadDict()
  const src = join(cwd, 'src')
  const skillByAtom = new Map<string, string>()
  walkSkillByAtom(src, skillByAtom)
  const atoms = [...skillByAtom.keys()].sort()

  const inDict = (w: string): boolean =>
    dict.has(w) ||
    SUFFIX.some(([suf, rep]) => w.length > suf.length + 2 && w.endsWith(suf) && dict.has(w.slice(0, -suf.length) + rep))
  const isCode = (w: string): boolean => /\d/.test(w) || (w.length <= 5 && /^[a-z]+$/.test(w))
  const isSchema = (w: string): boolean =>
    /schema\.org component word|Attested in schema\.org/.test(skillByAtom.get(w) || '')

  const grounded = new Set<string>()
  const ungrounded: string[] = []
  const groundable = (w: string, seen = new Set<string>()): boolean => {
    if (grounded.has(w) || inDict(w) || isCode(w) || isSchema(w) || DOMAIN.has(w)) return true
    if (seen.has(w)) return false
    seen.add(w)
    for (let i = 3; i <= w.length - 3; i++) {
      const l = w.slice(0, i)
      const r = w.slice(i)
      if ((inDict(l) || grounded.has(l) || atoms.includes(l)) && groundable(r, seen)) return true
    }
    return false
  }
  for (const a of atoms) {
    if (groundable(a)) grounded.add(a)
    else ungrounded.push(a)
  }
  return { atoms, grounded, ungrounded }
}

export function emitWordsTs(cwd: string = process.cwd()): number {
  const { atoms } = computeVocabulary(cwd)
  const dict = loadDict()
  const src = join(cwd, 'src')
  const skillByAtom = new Map<string, string>()
  walkSkillByAtom(src, skillByAtom)

  const inDict = (w: string): boolean =>
    dict.has(w) ||
    SUFFIX.some(([suf, rep]) => w.length > suf.length + 2 && w.endsWith(suf) && dict.has(w.slice(0, -suf.length) + rep))
  const isCode = (w: string): boolean => /\d/.test(w) || (w.length <= 5 && /^[a-z]+$/.test(w))
  const isSchema = (w: string): boolean =>
    /schema\.org component word|Attested in schema\.org/.test(skillByAtom.get(w) || '')

  const isRoot = (w: string): boolean => inDict(w) || isCode(w) || isSchema(w) || DOMAIN.has(w)
  const used = new Set<string>()
  const collect = (w: string, seen = new Set<string>()): boolean => {
    if (isRoot(w)) {
      used.add(w)
      return true
    }
    if (seen.has(w)) return false
    seen.add(w)
    for (let i = w.length - 3; i >= 3; i--) {
      const l = w.slice(0, i)
      if (isRoot(l) && collect(w.slice(i), seen)) {
        used.add(l)
        return true
      }
    }
    return false
  }
  for (const a of atoms) collect(a)
  const roots = [...used].sort()
  const header = `/**
 * GENERATED by src/vocabulary/emit.ts — do not edit by hand.
 *
 * THE SHARED VOCABULARY: the irreducible words erpax is allowed to use, each
 * grounded in a standard — the English dictionary, schema.org, a standard code,
 * or an established domain term. Every atom must be one of these or compose from
 * them ([[vocabulary]]). New words must extend this set deliberately. Regenerate:
 * tsx src/vocabulary/emit.ts --emit
 */

`
  writeFileSync(
    join(src, 'vocabulary', 'words.ts'),
    header + `export const SHARED_VOCABULARY: ReadonlySet<string> = new Set(${JSON.stringify(roots)})\n`,
  )
  return roots.length
}

if (import.meta.url === `file://${process.argv[1]}`) {
  const { atoms, grounded, ungrounded } = computeVocabulary()
  console.log(`vocab-gen — ${atoms.length} atoms · grounded ${grounded.size} · UNGROUNDED ${ungrounded.length}`)
  if (ungrounded.length) console.log('  ' + ungrounded.join(' '))
  if (process.argv.includes('--emit')) {
    const n = emitWordsTs()
    console.log(`emitted src/vocabulary/words.ts — ${n} roots`)
  }
}
