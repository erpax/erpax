/**
 * emit-ratchet — write ratchet.generated.ts from live scans + DOWN-only ratchet.
 *
 *   pnpm rules:ratchet
 *   tsx src/law/folder/emit-ratchet.ts
 */
import { existsSync, unlinkSync, writeFileSync } from 'node:fs'
import { join, relative } from 'node:path'
import {
  priorAxesForEmit,
  ratchetContentUuid,
  recomputeRatchetSnapshot,
} from './ratchet-compute'
import { RATCHET_AXES } from './ratchet-math'
import type { RatchetAxis } from './baseline-types'

const OUT = join(process.cwd(), 'src', 'law', 'folder', 'ratchet.generated.ts')
const HAND = join(process.cwd(), 'src', 'law', 'folder', 'ratchet.json')

/** Bootstrap emit from hand ratchet.json without full corpus scan (avoids init cycle). */
function bootstrapFromPrior(cwd: string): ReturnType<typeof recomputeRatchetSnapshot> {
  const prior = priorAxesForEmit(cwd)
  const axes: Partial<Record<RatchetAxis, number>> = {}
  for (const axis of RATCHET_AXES) {
    const v = prior[axis]
    if (v === undefined || !Number.isFinite(v)) {
      throw new Error(`emit-ratchet: missing prior axis "${axis}"`)
    }
    axes[axis] = v
  }
  return {
    contentUuid: ratchetContentUuid(axes),
    sealedAt: new Date().toISOString().slice(0, 10),
    axes,
  }
}

export function emitRatchet(
  cwd: string = process.cwd(),
  opts?: { fromLive?: boolean; bootstrap?: boolean },
): void {
  const prior = priorAxesForEmit(cwd)
  const snap = existsSync(HAND)
    ? bootstrapFromPrior(cwd)
    : recomputeRatchetSnapshot(cwd, prior, { bootstrap: opts?.bootstrap === true })
  const lines = [
    '/**',
    ' * AUTO-GENERATED by src/law/folder/emit-ratchet.ts — do not edit.',
    ' * Guardian ceilings computed from live corpus scans + DOWN-only ratchet.',
    ' * Re-run: pnpm rules:ratchet',
    ' *',
    ' * @see ./ratchet-compute — coordinate b2f75a6f (bypass-math)',
    ' */',
    "import type { RatchetAxis } from './baseline-types'",
    '',
    'export const RATCHET_GENERATED = {',
    `  contentUuid: ${JSON.stringify(snap.contentUuid)},`,
    `  sealedAt: ${JSON.stringify(snap.sealedAt)},`,
    '  axes: {',
    ...RATCHET_AXES.map((axis: RatchetAxis) => {
      const v = snap.axes[axis]
      return `    ${JSON.stringify(axis)}: ${v},`
    }),
    '  },',
    '} as const satisfies {',
    '  readonly contentUuid: string',
    '  readonly sealedAt: string',
    '  readonly axes: Readonly<Partial<Record<RatchetAxis, number>>>',
    '}',
    '',
  ]
  writeFileSync(OUT, lines.join('\n'))
  if (existsSync(HAND)) {
    unlinkSync(HAND)
    console.log(`removed hand ${relative(process.cwd(), HAND)}`)
  }
  console.log(
    `rules:ratchet — emitted ${relative(process.cwd(), OUT)} · uuid ${snap.contentUuid} · ${RATCHET_AXES.length} axes`,
  )
}

if (import.meta.url === `file://${process.argv[1]}`) {
  emitRatchet(process.cwd(), {
    fromLive: process.argv.includes('--from-live'),
    bootstrap: process.argv.includes('--bootstrap'),
  })
}
