#!/usr/bin/env node
/**
 * build-installed-catalogue — LOAD the installed Claude/Anthropic domain skills
 * into erpax, content-addressed, ready to MERGE with the erpax atom corpus.
 *
 * Walks the Claude desktop plugin roots (every `.claude-plugin/plugin.json` with a
 * sibling `skills/<name>/SKILL.md`) and emits
 * src/services/skill-router/installed.catalogue.ts:
 *   { domain, name, description, contentUuid }[]   (matches InstalledSkill in merge.ts)
 *
 * `domain` is the plugin's manifest name (finance, human-resources, …); the MERGE
 * (services/skill-router/merge.ts) normalises it to the erpax DomainAgent id and
 * unions it into SKILL_INDEX, deduped by route + contentUuid. The committed
 * `skills.index.ts` (erpax atoms) stays untouched and deterministic — the installed
 * catalogue is a SEPARATE, machine-local artifact, merged at seed/runtime.
 *
 * Degrades gracefully: no plugins found ⇒ an empty catalogue, so the build runs
 * everywhere (CI has no Claude desktop install). contentUuid is the SAME sha→uuid
 * as build-skill-index, so the same skill on any machine resolves to one identity.
 *
 * Usage: node src/services/skill-router/build-installed.mjs
 */

import { readdirSync, statSync, existsSync, readFileSync, writeFileSync, mkdirSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { homedir } from 'node:os'
import { createHash } from 'node:crypto'

const ROOT = join(homedir(), 'Library', 'Application Support', 'Claude', 'local-agent-mode-sessions')
const OUT = 'src/skill/router/installed.catalogue.ts'
const MAX_DEPTH = 8 // the manifests sit a few levels under session uuids; bound the walk

/** Deterministic v5-style uuid from content — identical to src/services/skill-router/build-index.mjs. */
function contentUuid(content) {
  const h = createHash('sha256').update(content).digest('hex')
  const y = ((parseInt(h[16], 16) & 0x3) | 0x8).toString(16)
  return `${h.slice(0, 8)}-${h.slice(8, 12)}-5${h.slice(13, 16)}-${y}${h.slice(17, 20)}-${h.slice(20, 32)}`
}

/** Frontmatter scalar (quoted + bare) — identical to build-skill-index. */
function fmValue(fm, key) {
  const m = fm.match(new RegExp(`^${key}:\\s*(.+)$`, 'm'))
  if (!m) return ''
  let v = m[1].trim()
  if ((v.startsWith('"') && v.endsWith('"')) || (v.startsWith("'") && v.endsWith("'"))) v = v.slice(1, -1)
  return v
}

/** Collect every plugin manifest dir (`.claude-plugin/plugin.json` present) under ROOT, depth-bounded. */
function findPluginDirs(dir, depth, out) {
  if (depth > MAX_DEPTH || !existsSync(dir)) return out
  if (existsSync(join(dir, '.claude-plugin', 'plugin.json')) && existsSync(join(dir, 'skills'))) out.push(dir)
  for (const name of readdirSync(dir)) {
    if (name === 'node_modules' || name.startsWith('.')) continue
    const full = join(dir, name)
    let isDir = false
    try { isDir = statSync(full).isDirectory() } catch { isDir = false }
    if (isDir) findPluginDirs(full, depth + 1, out)
  }
  return out
}

const byKey = new Map() // `${domain}/${name}` ⇒ entry (dedup across duplicate session copies)
for (const pluginDir of findPluginDirs(ROOT, 0, [])) {
  let domain = ''
  try { domain = JSON.parse(readFileSync(join(pluginDir, '.claude-plugin', 'plugin.json'), 'utf8')).name || '' } catch { domain = '' }
  if (!domain) continue
  const skillsDir = join(pluginDir, 'skills')
  for (const name of existsSync(skillsDir) ? readdirSync(skillsDir).sort() : []) {
    const file = join(skillsDir, name, 'SKILL.md')
    if (!existsSync(file)) continue
    const raw = readFileSync(file, 'utf8')
    const fm = (raw.match(/^---\n([\s\S]*?)\n---\n?/) || [, ''])[1]
    const key = `${domain}/${name}`
    if (byKey.has(key)) continue
    byKey.set(key, {
      domain,
      name: fmValue(fm, 'name') || name,
      description: fmValue(fm, 'description'),
      contentUuid: contentUuid(raw),
    })
  }
}

const catalogue = [...byKey.values()].sort((a, b) => a.domain.localeCompare(b.domain) || a.name.localeCompare(b.name))
const banner =
  '// AUTO-GENERATED by src/services/skill-router/build-installed.mjs — do not edit.\n' +
  '// The installed Claude domain-skill catalogue; merged into the erpax corpus by ./merge.ts.\n'
const file =
  banner +
  "import type { InstalledSkill } from './merge'\n\n" +
  `export const INSTALLED_CATALOGUE: readonly InstalledSkill[] = ${JSON.stringify(catalogue, null, 0)}\n`

mkdirSync(dirname(OUT), { recursive: true })
writeFileSync(OUT, file)
const domains = new Set(catalogue.map((c) => c.domain))
console.log(`installed-catalogue: wrote ${catalogue.length} skills across ${domains.size} domains → ${OUT}`)
